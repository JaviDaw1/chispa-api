package chispa.chispa.models.enums;

/**
 * Enum representing the gender of a user.
 * MALE: Male gender.
 * FEMALE: Female gender.
 * OTHER: Other or non-binary gender.
 */
public enum Gender {
    /**
     * Male gender
     */
    MALE,
    /** Female gender */
    FEMALE,
    /** Other or non-binary gender */
    OTHER
}
