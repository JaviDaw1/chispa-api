package chispa.chispa.models.enums;

/**
 * Enum representing the state of a like between users.
 * LIKED: The user has liked another user.
 * BLOCKED: The like is blocked.
 */
public enum LikeState {
    /**
     * The user has liked another user
     */
    LIKED,
    /** The like is blocked */
    BLOCKED
}
