package chispa.chispa.models.enums;

/**
 * Enum representing the preferred gender for user's favorite selection.
 * MALE: Prefers males.
 * FEMALE: Prefers females.
 * OTHER: Prefers other or non-binary genders.
 */
public enum FavoriteGender {
    /**
     * Prefers males
     */
    MALE,
    /** Prefers females */
    FEMALE,
    /** Prefers other or non-binary genders */
    OTHER
}
